      <h3><span><em>Dan Webb</em></span> <a href="http://twitter.com/danwrong">@danwrong</a></h3>
      <p>Dan Webb is a british JavaScript bloke working on the platform team at Twitter.  Recently he's spent a lot of time delving into the dark recesses of cross domain communication and security while working on @anywhere.  In the past, as director of Massive Robot, he's been involved in a wide range of projects including work for Channel 4, A Bathing Ape and People's Music Store as well as popular Twitter app, twaud.io.  When he's not doing that he's DJing, hording trainers and drinking fine beers.</p>
